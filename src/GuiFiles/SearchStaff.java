    private void SearchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchButtonActionPerformed
        try {
            Staff staff = Staff.searchStaff(Integer.parseInt(txtStaffid.getText()));
            lbmsg.setVisible(false);
            if(staff == null){
                JOptionPane.showMessageDialog(null,"Staff ID cannot be found");
            }
            else{
                txtnic.setText(String.valueOf(staff.getNic()));
                txtFname.setText(staff.getFname());
                txtLname.setText(staff.getLname());
                DOB.setDate(new SimpleDateFormat("yyyy-mm-dd").parse(staff.getDateOfBirth()));
                if(staff.getGender().equals("Female")){
                    rbFemale.setSelected(true);
                }
                else{rbMale.setSelected(true);}
                cmbType.setSelectedItem(staff.getType());
                txtHome.setText(String.valueOf(staff.getContact_home()));
                txtMobile.setText(String.valueOf(staff.getContact_mobile()));
                txtaAddr.setText(staff.getAddress());
                txtSpecialization.setText(staff.getSpecialization());
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_SearchButtonActionPerformed

