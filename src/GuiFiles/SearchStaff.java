    private void UpdateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateButtonActionPerformed
        try {
            Staff stf = new Staff(Integer.parseInt(txtStaffid.getText()),Integer.parseInt(txtnic.getText()),txtFname.getText(),txtLname.getText(),(String.format("%1$tY-%1$tm-%1$td", DOB.getDate())),(rbMale.isSelected()?"Male":"Female"), cmbType.getSelectedItem().toString(),Integer.parseInt(txtHome.getText()),Integer.parseInt(txtMobile.getText()),txtaAddr.getText(),txtSpecialization.getText());
            boolean found = stf.updateStaff();
            if(found==false){
             lbmsg.setText("Error : Staff no. cannot be found");
             lbmsg.setVisible(true);
            }
            else{
                lbmsg.setText("Updated succesfully");
                lbmsg.setVisible(true);
            
            }
        
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_UpdateButtonActionPerformed

