    private void SearchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchButtonActionPerformed
        try {
            Patient patient = Patient.searchPatient(Integer.parseInt(txtPatientid.getText()));
            if(patient == null){
                JOptionPane.showMessageDialog(null,"Error : Patient Id cannot be found");
            }
            else{
                txtnic.setText(String.valueOf(patient.getNic()));
                txtFname.setText(patient.getFirst_name());
                txtLname.setText(patient.getLast_name());
                DOB.setDate(new SimpleDateFormat("yyyy-mm-dd").parse(patient.getDateOfBirth()));
                if(patient.getGender().equals("Female")){
                    rbFemale.setSelected(true);
                }
                else{rbMale.setSelected(true);}
                
                txtHome.setText(String.valueOf(patient.getContact_home()));
                txtMobile.setText(String.valueOf(patient.getContact_mobile()));
                txtaAddr.setText(patient.getAddress());
                txtgrd.setText(patient.getGuardian());
                txtrel.setText(patient.getRelationship());
                DOA.setDate(new SimpleDateFormat("yyyy-mm-dd").parse(patient.getAdmission_date()));
                DOD.setDate(patient.getDischarge_date()==null?null:new SimpleDateFormat("yyyy-mm-dd").parse(patient.getDischarge_date()));
                txtpay.setText(String.valueOf(patient.getDue_payment()));
                txtrec.setText(patient.getExternal_phy());
                txtcon.setText(String.valueOf(patient.getConfirmation()));
                txtconid.setText(String.valueOf(patient.getConsultant()));
                txtwardno.setText(String.valueOf(patient.getWard_no()));
                cmbdis.setSelectedIndex(patient.getStatus()); //0 specifies the first item
                
                
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_SearchButtonActionPerformed

