    private void UpdateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateButtonActionPerformed
        try {
            Patient pat = new Patient(Integer.parseInt(txtPatientid.getText()),Integer.parseInt(txtnic.getText()),txtFname.getText(),txtLname.getText(),String.format("%1$tY-%1$tm-%1$td", DOB.getDate()),txtaAddr.getText(),txtgrd.getText(),txtrel.getText(),String.format("%1$tY-%1$tm-%1$td", DOA.getDate()),String.format("%1$tY-%1$tm-%1$td", DOA.getDate()),Double.parseDouble(txtpay.getText()),cmbdis.getSelectedIndex(),(rbMale.isSelected()?"Male":"Female"),Integer.parseInt(txtHome.getText()),Integer.parseInt(txtMobile.getText()),txtrec.getText(),Integer.parseInt(txtconid.getText()),Integer.parseInt(txtwardno.getText()),Integer.parseInt(txtcon.getText()));
            boolean found = pat.updatePatient();
            if(found==false){
             JOptionPane.showMessageDialog(null,"Error : Patient No. cannot be found");
            }
            else{
                JOptionPane.showMessageDialog(null,"Updated Successfully");
            
            }
        
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_UpdateButtonActionPerformed

